    private void jbInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInscribirActionPerformed
        // TODO add your handling code here:
        Materia materia = (Materia)jcbMaterias.getSelectedItem();
        Alumno estudiante = (Alumno)jbcAlumnos.getSelectedItem();
        
        estudiante.agregarMateria(materia);
            JOptionPane.showMessageDialog(this,"Inscripci√≥n Realizada Correctamente");
        
        if(materia == null||estudiante == null){
            JOptionPane.showMessageDialog(this,"Debe completar todos los campos");
        }
        else {
            JOptionPane.showMessageDialog(this,"Materia ya inscripta");
        }
    }//GEN-LAST:event_jbInscribirActionPerformed

    private void comboMateria(){
        HashSet<Materia> mat = Inscripciones.getMaterias();
        for (Materia m : mat) {
            jcbMaterias.addItem(m);
        }
        jcbMaterias.setSelectedIndex(-1);
    }
    private void comboAlumno() {
        HashSet<Alumno> al = Inscripciones.getEstudiantes();
        for (Alumno a : al) {
            jbcAlumnos.addItem(a);
        }
        jbcAlumnos.setSelectedIndex(-1);
    }

